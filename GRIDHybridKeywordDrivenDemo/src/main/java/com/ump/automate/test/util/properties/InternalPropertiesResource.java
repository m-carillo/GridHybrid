package com.ump.automate.test.util.properties;

/**
 * this class retrieved internal properties/fields from properties file
 * @author edward.cervantes
 */
public class InternalPropertiesResource {

    // LOCAL driver/browser
    private String chromeLocationProp = "";

    private String edgeLocationProp = "";

    private String geckoLocationProp = "";

    private String ieLocationProp = "";

    
    public String getChromeLocationProp() {
        return chromeLocationProp;
    }

    public void setChromeLocationProp(String chromeLocationProp) {
        this.chromeLocationProp = chromeLocationProp;
    }

    public String getEdgeLocationProp() {
        return edgeLocationProp;
    }

    public void setEdgeLocationProp(String edgeLocationProp) {
        this.edgeLocationProp = edgeLocationProp;
    }

    public String getGeckoLocationProp() {
        return geckoLocationProp;
    }

    public void setGeckoLocationProp(String geckoLocationProp) {
        this.geckoLocationProp = geckoLocationProp;
    }

    public String getIeLocationProp() {
        return ieLocationProp;
    }

    public void setIeLocationProp(String ieLocationProp) {
        this.ieLocationProp = ieLocationProp;
    }
}
